class ContactEntry {
   private String name;
   private String email;
   ContactEntry(String nameNew, String emailNew){
     name = nameNew;
     email = emailNew;
   }
    public ContactEntry(){
       name =null;
       name =null;
    }

   public void setemail(String emailNew){
     email=emailNew;
   }

   public void setname(String nameNew){
     name=nameNew;
   }
   public String getEmail(){
     return name;
   }

   public String getName(){
     return name;
   }

}
